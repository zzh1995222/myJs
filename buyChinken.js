/*百钱买百鸡问题*/
var i, j, k; //i,j,k分别为公鸡，母鸡，小鸡数量
/**
 * 公鸡，母鸡，小鸡数量满足以下方程
 * i + j + k = 100
 * 5i + 3j + k/3 = 100
 */
for(i=0; i<=20; i++){
    for(j=0; j<=33; j++){
        for(k=0; k<=100; k++){
            if(i+j+k === 100 && i*5+j*3+k/3 === 100){
                console.log("("+i+", "+j+", "+k+")");
            }
        }
    }
}